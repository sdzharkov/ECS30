#include <stdio.h>

int main(){
	
	printf("5 + 9 = %d\n", 5 + 9); //to do addition use +
	printf("5 - 9 = %d\n", 5 - 9); //to do subtraction use -
	printf("5 * 9 = %d\n", 5 * 9); //to do multiplication use *
	printf("13 / 3= %d\n", 13 / 3); //to do multiplication use /. 
																   //The type of division depends on the type of the number. 
																   //Sice we have two integers we are doing integer division
	printf("The remainder after dividing 13 by 3 is %d\n", 13 % 3); //% is for remainder. 																 

	return 0;
}
